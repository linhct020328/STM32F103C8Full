#include "main.h"
#include "usb_device.h"

#include "usbd_hid.h"
#include "KEYPAD.h"

uint8_t report[8] = {
	0, //MODIFIER
	0, //RESERVED
	0, //KEYCODE1
	0, //KEYCODE2
	0, //KEYCODE3
	0, //KEYCODE4
	0, //KEYCODE5
	0  //KEYCODE6
}; 
extern USBD_HandleTypeDef hUsbDeviceFS;
char Key;
KEYPAD_Name KeyPad;
char KEYMAP[NUMROWS][NUMCOLS] = {
{0x1E,0x1F,0x20},
{0x21,0x22,0x23},
{0x24,0x25,0x26},
{0x55,0x27,0x58}
};

void SystemClock_Config(void);
static void MX_GPIO_Init(void);

int main(void)
{
  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_USB_DEVICE_Init();
	
	KEYPAD3X4_Init(&KeyPad, KEYMAP, GPIOA, GPIO_PIN_4, GPIOA, GPIO_PIN_5, GPIOA, GPIO_PIN_6, 
																	GPIOA, GPIO_PIN_0, GPIOA, GPIO_PIN_1,
																	GPIOA, GPIO_PIN_2, GPIOA, GPIO_PIN_3);
	
  while (1)
  {
    Key = KEYPAD3X4_Readkey(&KeyPad);
		if(Key)
		{
			report[2] = Key;
			USBD_HID_SendReport(&hUsbDeviceFS, report, sizeof (report)); // gui ki tu 
			HAL_Delay(50);
			
			Key = 0; 
			report[2] = 0;
			USBD_HID_SendReport(&hUsbDeviceFS, report, sizeof (report)); // dung gui ki tu
			HAL_Delay(50);
		}
  }
  
}

